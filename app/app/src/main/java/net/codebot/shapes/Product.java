package net.codebot.shapes;

public class Product {
    String id;
    String name;
    String pic;
    int quantity;
    String location;

    public Product(String id, String name, String pic, int quantity, String location){
        this.id=id;
        this.name=name;
        this.pic=pic;
        this.quantity=quantity;
        this.location=location;
    }
}
